CREATE VIEW V_HJAB AS
SELECT M04.Kode,M04.Keterangan,M04.Singkatan,M04.Recs,M04.Created_By,M04.Updated_By,M04.Updated_On,M05.FlgDpPt,M03.NmDpPt,
M05.Persen,M05.Nilai,M05.KdCurr,M40.KTCURR, M05.Trans
FROM M04HJAB M04
LEFT JOIN M05DJAB M05 ON M05.Kode=M04.Kode
LEFT JOIN M03DPPT M03 ON M03.FlgDpPt=M05.FlgDpPt AND M03.KdDpPt=M05.KdDpPt
LEFT JOIN M40CURR M40 ON M40.KDCURR=M05.KdCurr
