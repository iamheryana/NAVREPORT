CREATE VIEW V_HKJB AS
SELECT M06.Kode,M06.Keterangan,M06.Singkatan,M06.Recs,M06.Created_By,M06.Updated_By,M06.Updated_On,
M07.FlgDpPt,M03.NmDpPt,M07.Persen,M07.Nilai,M07.KdCurr,M40.KTCURR, M07.Trans
FROM M06HKJB  M06 LEFT JOIN M07DKJB M07 ON M06.Kode=M07.Kode
LEFT JOIN M03DPPT M03 ON M03.FlgDpPt=M07.FlgDpPt AND M03.KdDpPt=M07.KdDpPt
LEFT JOIN M40CURR M40 ON M40.KDCURR=M07.KdCurr
