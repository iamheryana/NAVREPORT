CREATE VIEW V_HGOL AS
SELECT M12.Kode,M12.Keterangan,M12.Singkatan,M12.MinGol,M12.MidGol,M12.MaxGol,M12.Created_By,M12.Updated_By,M12.Updated_On,
M13.FlgDpPt,M13.KdDpPt,M03.NmDpPt,M13.Persen,M13.Nilai,M13.KdCurr,M40.KTCURR, M13.Trans
FROM M12HGOL M12
LEFT JOIN M13DGOL M13 ON M13.Kode=M12.Kode
LEFT JOIN M03DPPT M03 ON M03.FlgDpPt=M13.FlgDpPt AND M03.KdDpPt=M13.KdDpPt
LEFT JOIN M40CURR M40 ON M40.KDCURR=M13.KdCurr